#!/usr/bin/env node
//
// regenerate-emoji-aliases.js
//
// Regenerates tools/markdown-kit/slack-emoji-aliases.json by diffing
// emoji-datasource's Slack/Discord short_names against gemoji (GitHub's
// official dictionary). The output contains shortcodes that users type
// in Slack-style (e.g., :robot_face:, :thinking_face:) but that aren't
// in GitHub's gemoji dictionary.
//
// Run this when Unicode releases new emoji (roughly annually, September).
// The tool warns at runtime if the JSON is older than 6 months.
//
// Usage: node tools/markdown-kit/scripts/regenerate-emoji-aliases.js
//
// Installs emoji-datasource and gemoji temporarily in an isolated tmp dir,
// so no dev dependencies need to live in the main project.

import { execSync } from 'node:child_process';
import { existsSync, writeFileSync, rmSync, mkdirSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { tmpdir } from 'node:os';
import { fileURLToPath } from 'node:url';
import { createRequire } from 'node:module';

const SCRIPT_DIR = dirname(fileURLToPath(import.meta.url));
const MARKDOWN_KIT_DIR = dirname(SCRIPT_DIR);
const OUTPUT_PATH = join(MARKDOWN_KIT_DIR, 'slack-emoji-aliases.json');

// Isolated temp dir for dependencies
const WORK_DIR = join(tmpdir(), `regenerate-emoji-aliases-${process.pid}`);

function cleanup() {
  if (existsSync(WORK_DIR)) {
    try { rmSync(WORK_DIR, { recursive: true, force: true }); } catch {}
  }
}

process.on('exit', cleanup);
process.on('SIGINT', () => { cleanup(); process.exit(1); });

try {
  // 1. Install deps in isolated tmp dir
  console.log('Installing emoji-datasource and gemoji...');
  mkdirSync(WORK_DIR, { recursive: true });
  // Create a minimal package.json to avoid npm complaints
  writeFileSync(join(WORK_DIR, 'package.json'), '{"name":"regen-tmp","version":"1.0.0"}');
  execSync('npm install --silent --no-save emoji-datasource@latest gemoji@latest', {
    cwd: WORK_DIR,
    stdio: ['pipe', 'pipe', 'inherit'],
  });

  // 2. Load the packages
  const require = createRequire(join(WORK_DIR, 'package.json'));
  const emojiData = require('emoji-datasource');
  const { nameToEmoji } = require('gemoji');

  // 3. Get the emoji-datasource version for metadata
  const emojiDatasourcePkg = require('emoji-datasource/package.json');
  const emojiDatasourceVersion = emojiDatasourcePkg.version;

  // 4. Diff: find short_names NOT in gemoji
  const aliases = {};
  for (const entry of emojiData) {
    // Note: skin-tone variants are nested under the base entry's skin_variations property,
    // NOT separate top-level entries. The base entry (default yellow) is what we want.
    const codepoints = entry.unified.split('-').map(cp => parseInt(cp, 16));
    const char = String.fromCodePoint(...codepoints);
    for (const name of entry.short_names) {
      if (!nameToEmoji[name]) {
        aliases[name] = char;
      }
    }
  }

  // 5. Sort alphabetically
  const sortedAliases = {};
  for (const name of Object.keys(aliases).sort()) {
    sortedAliases[name] = aliases[name];
  }

  // 6. Write the output with metadata
  const output = {
    _emoji_datasource_version: emojiDatasourceVersion,
    _generated_at: new Date().toISOString(),
    _description: 'Slack/Discord emoji shortcodes not in GitHub gemoji. Generated by regenerate-emoji-aliases.js.',
    aliases: sortedAliases,
  };
  writeFileSync(OUTPUT_PATH, JSON.stringify(output, null, 2) + '\n');

  // 7. Report
  const count = Object.keys(sortedAliases).length;
  const sizeKB = (JSON.stringify(output).length / 1024).toFixed(1);
  console.log(`Generated ${count} aliases from emoji-datasource v${emojiDatasourceVersion}`);
  console.log(`Wrote ${sizeKB} KB to ${OUTPUT_PATH}`);
} catch (e) {
  console.error('Error:', e.message);
  process.exit(1);
}